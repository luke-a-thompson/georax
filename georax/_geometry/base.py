from __future__ import annotations

from abc import abstractmethod
from typing import Any, Generic, Literal, TypeVar

import equinox as eqx
from diffrax._custom_types import RealScalarLike
from jaxtyping import Array

chart_order = int | Literal["exact"]
Geometry = TypeVar("Geometry", bound="Manifold[Any]")


class LocalChart(eqx.Module, Generic[Geometry]):
    order: RealScalarLike
    inverse_order: RealScalarLike

    @abstractmethod
    def apply(self, x: Array, a: Array, geometry: Geometry) -> Array:
        """Apply the chosen local chart at x for coefficients a."""
        ...

    def inverse_differential(
        self, x: Array, a: Array, b: Array, geometry: Geometry
    ) -> Array:
        """Apply the inverse differential of ``a -> apply(x, a, geometry)``."""
        raise NotImplementedError(
            f"{type(self).__name__} does not implement inverse_differential."
        )


class Manifold(eqx.Module, Generic[Geometry]):
    """Minimal manifold geometry backend for retraction-based integrators.

    This class contains only geometric primitives and does not contain solver
    logic. It is sufficient for retraction-based manifold Runge--Kutta schemes
    such as the current commutator-free solvers.
    """

    chart: LocalChart[Geometry] | None = eqx.field(static=True, default=None)

    @property
    @abstractmethod
    def state_shape(self) -> tuple[int, ...]:
        ...

    @property
    @abstractmethod
    def coordinate_shape(self) -> tuple[int, ...]:
        ...

    def check_state_shape(self, x: Array) -> None:
        if x.shape != self.state_shape:
            raise ValueError(f"{type(self).__name__} state must have shape {self.state_shape}; got {x.shape}.")

    def check_coordinate_shape(self, a: Array) -> None:
        if a.shape != self.coordinate_shape:
            raise ValueError(f"{type(self).__name__} coordinates must have shape {self.coordinate_shape}; got {a.shape}.")

    def apply_increment(self: Geometry, x: Array, a: Array) -> Array:
        """Apply the finite motion generated by frame coefficients a at x.

        If a local chart model is attached, then it is used directly.
        """
        self.check_state_shape(x)
        self.check_coordinate_shape(a)
        if self.chart is not None:
            return self.chart.apply(x, a, self)
        chart = self.select_chart(12)
        return chart.apply(x, a, self)

    @abstractmethod
    def select_chart(
        self: Geometry, required_order: RealScalarLike
    ) -> LocalChart[Geometry]:
        """Choose a local chart based on the order required by the solver."""
        ...
